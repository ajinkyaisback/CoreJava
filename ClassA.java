package JavaBasics;

public class ClassA {
		
 public ClassA() {
	 
	System.out.println("Parent Class Constructor");
 }
 
 public ClassA(int a) {
	 
	 System.out.println("Parent Class Constructor with Value of a is  "+ a) ;
 }
 
public ClassA(int k, int j) {
	 
	 System.out.println("Parent Class Constructor with Value of k is  "+ k);
	 System.out.println("Parent Class Constructor with Value of j is  "+ j);
 }
 
 
 
}
